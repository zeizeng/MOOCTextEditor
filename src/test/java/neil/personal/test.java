public class test {
    public static void main(String[] args) {
        String a = "Here is a series of test sentences. Your program should find 3 sentences, 33 words, and 49 syllables. Not every word will have the correct amount of syllables (example, for example), but most of them will.";
        String[] array = a.split("[. 0-9,()]+");
        Pattern pattern = Pattern.compile("[AIOUYaiouy]*[eE]+[AIOUYaiouy]+[eE]*|[AIOUYaiouy]+|[AIOUYaiouy]*[eE]+\\B");
        for (String s : array) {
            Matcher matcher = pattern.matcher(s);
            System.out.print(s + "\t");
            while (matcher.find()) {
                System.out.print(matcher.group() + "\t");
            }
            System.out.println();
        }

    }
}
